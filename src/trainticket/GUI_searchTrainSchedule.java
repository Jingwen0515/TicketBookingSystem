    // Display All Info
    public void displayTable(){
        // read dataline from file
        DefaultTableModel model = (DefaultTableModel) ScheduleTable.getModel();
        FileManager getrow = new FileManager("src/Assets/trainschedules.txt");
        ArrayList<String[]> rows =  getrow.saveTo2DArrayList();
        
        for(String[] row: rows){
            if(row[6].equals("Pending")){
                model.addRow(row);
            }
        }
    }
    // Display Search Info
    public void displayTable(ArrayList<String[]> scheduleData){
        //[{},{},{},....]
        //ArrayLIst with String Array
        DefaultTableModel model = (DefaultTableModel) ScheduleTable.getModel();
        for(int i =0;i<scheduleData.size();i++){
            String[] schedule = scheduleData.get(i);
            if(schedule[6].equals("Pending")){
                model.addRow(schedule);
            }
        }
    }
    
    public void removeTableRow(){
        DefaultTableModel model = (DefaultTableModel) ScheduleTable.getModel();
        int count = model.getRowCount();
        for (int i = count - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }
    
