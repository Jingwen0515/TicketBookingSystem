    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        String targetSource = SourceText.getText();
        String targetDestination = DestinationText.getText();
        
        // only source
        if(!targetSource.isEmpty() && targetDestination.isEmpty()){
            FileManager getrow = new FileManager("src/Assets/trainschedules.txt");
            ArrayList<String[]> rows =  getrow.saveTo2DArrayList();
            ArrayList<String[]> scheduleData = new ArrayList<>();
            for(String[] row: rows){
                if(row[4].equals(targetSource)){
                    scheduleData.add(row);
                }
            }
            removeTableRow();
            displayTable(scheduleData);

        }
        // only destination
        else if(targetSource.isEmpty() && !targetDestination.isEmpty()){
            FileManager getrow = new FileManager("src/Assets/trainschedules.txt");
            ArrayList<String[]> rows =  getrow.saveTo2DArrayList();
            ArrayList<String[]> scheduleData = new ArrayList<>();
            for(String[] row: rows){
                if(row[5].equals(targetDestination)){
                    scheduleData.add(row);
                }
            }
            removeTableRow();
            displayTable(scheduleData);
        }
        // both source and destination
        else if(!targetSource.isEmpty() && !targetDestination.isEmpty()){
            FileManager getrow = new FileManager("src/Assets/trainschedules.txt");
            ArrayList<String[]> rows =  getrow.saveTo2DArrayList();
            ArrayList<String[]> scheduleData = new ArrayList<>();
            for(String[] row: rows){
                if(row[4].equals(targetSource) && row[5].equals(targetDestination)){
                    scheduleData.add(row);
                }
            }
            removeTableRow();
            displayTable(scheduleData);
        }
        // no input
        else{
            removeTableRow();
            displayTable();
        }
        
        
        
    }//GEN-LAST:event_SearchButtonActionPerformed

